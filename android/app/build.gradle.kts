            it.name.startsWith("merge") &&
            it.name.contains("JniLibFolders")
    }.configureEach {
        dependsOn("buildRustCore")
    }

// Read config from the project root .env file (generated by `make setup-env`)
val envFile =
    rootProject.file("../.env").let { f ->
        mutableMapOf<String, String>().also { map ->
            if (f.exists()) {
                f.readLines().forEach { line ->
                    val trimmed = line.trim()
                    if (trimmed.isNotEmpty() && !trimmed.startsWith("#") && '=' in trimmed) {
                        val (k, v) = trimmed.split("=", limit = 2)
                        map[k.trim()] = v.trim()
                    }
                }
            }
        }
    }

val appEnv = project.findProperty("APP_ENV") as String? ?: envFile["APP_ENV"] ?: "dev"
val isProd = appEnv == "prod"

