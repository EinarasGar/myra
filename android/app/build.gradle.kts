    }

    // Auto-select flavor based on APP_ENV in .env
    androidComponents {
        beforeVariants { variant ->
            val flavorName = variant.productFlavors.find { it.first == "environment" }?.second
            // Disable variants that don't match the current env
            if (flavorName != null && flavorName != appEnv) {
