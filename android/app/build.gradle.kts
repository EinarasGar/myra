                ?: "http://localhost:$serverPort"
        buildConfigField("String", "API_BASE_URL", "\"$apiBaseUrl\"")
    }

    signingConfigs {
        create("app") {
            // Resolves absolute paths as-is, relative paths from project root
            val storePath =
                project.findProperty("SVERTO_STORE_FILE") as String?
                    ?: envFile["SVERTO_STORE_FILE"]
            storeFile =
                storePath?.let { path ->
                    val f = file(path)
                    if (f.exists()) f else rootProject.file("../$path")
                }
            storePassword = project.findProperty("SVERTO_STORE_PASSWORD") as String?
                ?: envFile["SVERTO_STORE_PASSWORD"] ?: ""
            keyAlias = project.findProperty("SVERTO_KEY_ALIAS") as String?
                ?: envFile["SVERTO_KEY_ALIAS"] ?: ""
            keyPassword = project.findProperty("SVERTO_KEY_PASSWORD") as String?
                ?: envFile["SVERTO_KEY_PASSWORD"] ?: ""
        }
    }

